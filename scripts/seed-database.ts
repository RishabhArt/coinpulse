export async function seedDatabase() {
  try {
    console.log('🚀 Initializing database...');
    await initializeDatabase();
    
    console.log('📊 Seeding coins data...');
    for (const coin of mockCoins) {
      await CoinService.upsertCoin(coin);
    }
    
    console.log('📈 Generating OHLC data...');
    for (const coin of mockCoins) {
      await OHLCService.generateMockOHLCData(coin.id, 7); // 7 days of data
    }
    
    console.log('🏷️ Seeding categories...');
    await DataSeeder.seedDatabase();
    
    console.log('✅ Database seeded successfully!');
  } catch (error) {
    console.error('❌ Database seeding failed:', error);
    throw error;
  }
}

// Run seeding if called directly
if (require.main === module) {
  seedDatabase()
    .then(() => process.exit(0))
    .catch(() => process.exit(1));
}
